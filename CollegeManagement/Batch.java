    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        String batchname = txtBatch.getText();
        String year = txtYear.getText();
        String id = txtId.getText();
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
             con =  (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/collegemanagement","root","root");
             pst = (PreparedStatement) con.prepareStatement("insert into batch(id,batchname,year)values(?,?,?)");
                
                pst.setString(1, id);
                pst.setString(2, batchname);
                pst.setString(3, year);
             
                pst.executeUpdate();
                
                JOptionPane.showMessageDialog(null,"Batch Created");
                
                txtBatch.setText("");
                txtYear.setText("");
                txtId.setText("");
                txtBatch.requestFocus();
    
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Batch.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Batch.class.getName()).log(Level.SEVERE, null, ex);
        }
  
    }//GEN-LAST:event_jButton1ActionPerformed

