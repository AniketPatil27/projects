    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            
        
        try {
            
            if(txtUser.getText().isEmpty() || txtPassword.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(this,"Username or Password not entered");
            }
            
            else{
                
                String username = txtUser.getText();
                String password = txtPassword.getText();
            Class.forName("com.mysql.jdbc.Driver");
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/collegemanagement","root","root");
            pst=(PreparedStatement) con.prepareStatement("select * from user where username=? and password=?");
            
            pst.setString(1, username);
            pst.setString(2, password);
            
            rs=pst.executeQuery();
            
            if(rs.next())
            {
                Main m = new Main();
                this.hide();
                m.setVisible(true);
                
            }
            else{
                JOptionPane.showMessageDialog(this,"Username or Password Incorrect");
                txtUser.setText("");
                txtPassword.setText("");
                
                txtUser.requestFocus();
            }
            
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

