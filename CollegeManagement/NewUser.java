    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(txtUser.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(this,"Please Type User Name");
        }
        else if(txtPassword.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(this,"Please Enter Password");
        }
        else if(txtPassword.getText().equals(txtConfirmPassword.getText())==false)
        {
            JOptionPane.showMessageDialog(this,"Password Not Matched");
        }
        
        else
        {
            try {
                String username=txtUser.getText();
                String confirmpass=txtConfirmPassword.getText();
                
                Class.forName("com.mysql.jdbc.Driver");
                con =  (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/collegemanagement","root","root");
                pst = (PreparedStatement) con.prepareStatement("insert into user(username,password)values(?,?)");
                
                pst.setString(1, username);
                pst.setString(2, confirmpass);
               
                pst.executeUpdate();
                
                JOptionPane.showMessageDialog(null,"Registered");
                
                txtUser.setText("");
                txtPassword.setText("");
                txtConfirmPassword.setText("");
               
                txtUser.requestFocus();
                
                
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(NewUser.class.getName()).log(Level.SEVERE, null, ex);    
            } catch (SQLException ex) {
                Logger.getLogger(NewUser.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

