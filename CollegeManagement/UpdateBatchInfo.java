    public void input()
    {
        try {
       
            Class.forName("com.mysql.jdbc.Driver");
            con =  (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/collegemanagement","root","root");
            pst=(PreparedStatement) con.prepareStatement("select * from batch where id=?");
           
            pst.setString(1, Id1);
            rs=pst.executeQuery();
       
            while(rs.next())
            {
               txtBatch.setText(rs.getString("batchname"));
                txtYear.setText(rs.getString("year"));
            }
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(StudentRegistration.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(StudentRegistration.class.getName()).log(Level.SEVERE, null, ex);
        }  
    }
    
    
